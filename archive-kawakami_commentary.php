              <h3 class="pt-4">アメリカは民主主義を貫けるか。鍵握るバイデン政権の「人権外交」</h3>
              <p class="text-right">川上高司「The Tokyo Post 2022年1月28日より」</p>
              <p>
              　ジョー・バイデン政権は《「人権」はアメリカ外交の中核である》と公言してはばからない（ブリンケン国務長官2021年2月）。アメリカにとって「人権」は核心的なものであり、その「人権外交」は中国やロシアの共産主義に対抗するための「錦の御旗」である。
              </p>
              <h4>1．アメリカにとっての「人権」</h4>
              <p>そもそも民主主義の中核をなす「人権」なくしては、アメリカの国家そのものが成り立たない。ジェファーソン大統領が独立宣言で民主主義の基本を述べ、それが憲法に反映された国家である。アメリカとは、世界中から「自由」の国アメリカを目指しやってくる移民が作り上げた国家である。アメリカの総人口に占める人種構成は、2020年時点で白人は57.8％、ヒスパニック系18.7％、黒人12.1％であり、白人の割合が年々減少し、21世紀半ばには白人はマイノリティーとなる。<br>
              　その多民族国家を結束させているのが「民主主義」である。したがって、民主主義という「錦の御旗」が無くなれば、アメリカは崩壊する。<br>
              　もともと、アメリカは「神の国」を作ろうとピューリタン（清教徒）がイギリスから米東海岸のプリマスに植民して出来た民主主義国家である。その後、アメリカは世界各国から「自由」を求めてやってくる移民国家として発展した。したがって、多民族国家をまとめるのは「民主主義」であり、その要素を欠けばアメリカは分裂する。<br>
              　アメリカは現在、分裂の危機にある。その背景には、白人至上主義を掲げたトランプ政権にあり、バイデン政権になってからも解消はされず、昨年11月8日の中間選挙に突入する。現在、米国では一部の州で黒人などマイノリティーの投票権を事実上制限しようとする動きや、2020年に全米で拡散した人種差別への抗議運動BLM（ブラック・ライブズ・マター）もアメリカの分裂の要因となっている。<br>
              　そして、その解決の処方箋は「人権」にある。「人権」を今一度アメリカ国民に想起させ、民主主義の重要性を米国の同盟国・友好国に思い出させ、アメリカを中心とする「神の世界」を取り戻すことにある。事実、バイデン大統領は12月9日の「民主主義サミット」で、米国の民主主義を「社会の分断を癒やす継続的な闘い」と述べ、現在の米国内での民主主義の危機を訴えた。<br>
              </p>
              <h4>２．人権外交の復活</h4>
              <p>
              　外交政策でみるならば、第一次世界大戦を経て、ウィルソン大統領が国内政策だけでなく対外政策においても、民主主義という価値が重要な要素だと唱え、多くの国家が賛同してきた。
              ウィルソン大統領は、1918年に「14箇条の原則」で、道徳性と倫理を基盤とする民主主義外交を主張した。それまで多くの諸国が外交政策の指針は自己の国益だけだと考えていたのに対して異を唱え、それ以降、民主主義が世界秩序を形成するとしたのである。
              その後、「民主主義国家」のアメリカは西側の代表として、「社会主義国家」を代表するソ連と冷戦を戦い勝利を収めたのである。政治学者のフランシス・フクヤマは冷戦終結で「歴史は終わった」と宣言したのであるが、中国の台頭を見た。
              アメリカの伝統的な人権外交を真っ向から否定したのが、トランプ前大統領であった。中国との争いに対して、トランプ大統領は「民主主義」対「共産主義」との闘争とはみずに、「覇権争い」として対峙した。そして、従来のNATOなどの民主主義同盟は無視し、アメリカ一国のいわゆる「パワー・ポリティクス」で中国との新冷戦を開始した。
              トランプ大統領は「人権」を軽視する一方、国益を全面に出すアメリカファースト（米国第一主義）の外交政策を展開した。そのため、民主主義同盟は希薄化し、国連や北太平洋条約機構（NATO）などこれまで冷戦後、アメリカが作り上げてきた秩序は崩壊の危機にあった。
              その立て直しをバイデン大統領は行っている。<br>
              　バイデン大統領は就任演説で、民主主義を無視したトランプ前大統領政権に対して、「米国内で民主主義が勝利を収めた」と宣言し、「民主主義を守る」決意を表明した。これは、民主主義同盟国と修復を行い世界秩序を取り戻す意志表明であったが、これは中国やロシアへの対抗海外向けのメッセージだけではなく、民主的価値を否定する世界への影響力拡大を図っていると、民主的な同盟国・パートナー国にむけた協調へのメッセージだ。
              バイデン大統領は、トランプ政権で離脱した条約や協定を復活させ、民主主義外交を復活させる。事実、大統領就任と同時に、トランプ大統領が行ったアメリカファースト（米国第一主義）の政策を大統領令で再転換した。
              </p>
              <h4>３．バイデン政権の「人権外交」の人事</h4>
              <p>
              　1942年生まれのバイデン大統領は「民主主義」の重要性を躰の芯まで叩きこみ、冷戦時代を勝ち抜いた勇者である。そして民主党の重鎮中の重鎮であり、遅咲きに大統領となった。しかし、その信念である民主主義の重要性を自らの人生に刻み込んだ「信念の人」であり、最後の生き残りといってもいいかもしれない。<br>
              　バイデン大統領は、大統領就任早々、トランプ大統領が2018年に離脱した国連人権理事会のオブザーバーへの復帰を行った。<br>
              　人権外交を支える閣僚は女性やマイノリティーが任命された。国連大使に黒人女性のリンダ・トマス・グリーンフィードを据えた。そして、国家安全保障局（NSC）に民主主義・人権担当調整官を新設し、全米民主主義基金のシャンティ・カラシル、国務省では国務次官（民間人保護・民主主義・人権担当）に超党派国際ネットワークのAfP（Alliance for Peacebuilding）会長兼CEOのウズラ・ゼヤ、米国際開発庁（USAID）長官に元国連大使のサマンサ・パワーを指名した。また、カラシルはチベット問題担当の特別調整官も兼務する。<br>
              　また、バイデン政権には政治的にはリベラルだが、人権や民主主義のための米国の軍事介入を支持するスクールを「リベラル・ホーク」が多い。<br>
              　大統領の右腕であるアントニー・ブリンケン国務長官も「リベラル・ホーク」であり、ホロコーストを体験した東欧系ユダヤ人の継父の影響で、ブリンケンの人道的介入主義が形成されたと言われる。ブリンケンは、クリントン政権で働いた後、バイデンが上院外交委員長の時にスタッフとなり係わりあいを持った。その後、オバマ政権で国家安全保障副補佐官を務め、人道的介入論者としてシリア内戦やリビア内戦への積極関与を強く推し進めた。<br>
              　さらに、「リベラル・ホーク」には、国務省副長官に次ぐナンバー3の政務担当国務次官に、ビクトリア・ヌーランド元NATO大使が指名された。彼女の夫はネオコンの論客のブルッキングス研究所上級研究員ロバート・ケーガンである
              </p>
              <h4>４．バイデンの人権外交</h4>
              <p>
              　バイデン政権の直面する最大の課題は、中国との多岐にわたる力関係である。トランプ政権はアメリカ一国で展開する「パワー・ポリテイクス」であったが、バイデン政権は同盟国とともに展開する「バランス・オブ・パワー」である。台頭する中国に対してアメリカ一国ではヘッジできなくなっているため民主主義同盟を用いるのである。<br>
              　バイデン政権は、従来の民主党政権に特徴的な非軍事的な「ソフト・パワー」政策を展開する。その中核にリベラルな「人権外交」がある。これは、軍事力によるハード・パワーをとる共和党政権とは異なる。<br>
              　そして、バイデン政権の「人権外交」は、国際政治における対決の手段として展開される場合と、人権という価値の浸透という2つの側面がある。前者の場合は制裁となり、後者の場合はクリントン政権で展開された関与政策となる。<br>
              　バイデン政権の対中人権外交は、前者と後者の2つの側面が交錯するがどうしても前者が浮き彫りとなる。<br>
              　その戦略は、「新疆ウイグル」と「香港」を具体的な救済すべきターゲットとして、民主主義陣営で中国への圧力を加えることにある。その目的は中国に人権を改善させるとともに民主主義諸国を束ねることにある。また、新疆ウイグル自治区は綿花などの国際的サプライチェーンともなり、そこに関与する日本などの西側企業のあぶり出しともなる。<br>
              　バイデン政権は、昨年12月にウイグル強制労働防止法案に署名し強制労働で生産されたものではないと企業が証明できる場合を除き、中国・新疆ウイグル自治区からの産品の輸入を禁止したりしている。そして、その運動は中国のサプライチェーンをデカップリングする経済安全保障政策とも結びつきながら同盟国や友好国をも巻き込み民主主義同盟の対中政策として展開されつつある。<br>
              　しかしながら、人権を前面に掲げることは、同盟国との連帯を強化することになる一方で、中国やロシア、それに北朝鮮といった権威主義国の連携を強めさせることにもなる。<br>
              　中国は早速、経済的つながりの深いイタリアやフランスなどに民主主義同盟の結束を寸断すべく、アプローチをかけている。また、米国との相対的パワーの増大を行う中国は、積極的に経済力を用いながら、「人権」の相対化を行っている。<br>
              　中国にだけ人権外交を繰り広げるのかという相対化を問うているのである。バイデン政権の初期に起こったミャンマーの政変があり、それは民主化への道を逆転させるものであったが放置した。アフリカや中東地域で人権状況が後退しているがこれも見て見ぬふりである。本当にアメリカは人権が侵害された諸国や地域に軍事的バックアップを行うのか、が疑問視されている。香港での人権や民主主義の抑圧に対してアメリカはなすすべを知らなかった。<br>
              　また、中国はアメリカ国内の人権問題にも堂々と指摘するようになった。国際社会での人権規範を堅持させるためには、アメリカ国内で人権が損なわれないように対応することが不可欠となる。テロ対策を強化することで、イスラム系国民や移民の人権が侵害され、治安強化が行われ、マイノリティーの人権が侵害された。また、メキシコとの国境を乗り越えようとする難民・移民を馬に乗った警察官が追い払うという状況や、<br>
              　「ブラック・ライブズ・マター（黒人の命を軽んじるな）」をスローガンとするデモ隊を全米で警官隊や州兵が暴力で制圧している事など枚挙にいとまがない。<br>
              　これらの、人権外交をめぐる米中の相克を民主主義国家はどうみるか、バイデン政権の「人権外交」の手腕が問われている。現在、中間選挙を目前に控えたバイデン大統領の支持率は、33％という最低のものでありどう支持率を回復するかも大きな課題である。<br>
              </p>
