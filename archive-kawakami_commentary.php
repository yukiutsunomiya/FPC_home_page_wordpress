              <h3>欧米の対ロ経済制裁は有効なのか？―「力なき外交」の限界―</h3>
              <p class="text-right">川上高司「The Tokyo Post 2022年2月23日より」</p>
              <p>
              　ロシアのウクライナ進攻危機に対して、米国を中心とするG7や欧州安全保障会議からは力強さが全く感じられない。いくらロシアを非難しようと、「力なき外交は無力である」。そこには軍事的抑止を最初から放棄する「弱さ」しかない。
              </p>
              <h4>欧米の「弱腰な」対ロ制裁と露中接近</h4>
              <p>
                　欧米諸国はロシアがウクライナに侵攻すれば、「（ロシアの）最大かつ最も重要な金融機関に激しい圧力」を科すという警告を繰り返すばかりである。米国を始めとする民主主義同盟諸国はロシアのハード・パワー力（軍事力）に対してソフトパワー（金融・経済力）で抑止しようとしているが、どこまで有効なのであろうか？　ロシアの仕掛けるオールドメインウオフェアー（超限戦）に対して全く形成不利である。<br>
                　さらに、ロシアは中国と戦略的パートナーとしてオールドメイン（外交・国防・情報・財務当局などのすべての領域）で共同歩調を取り始めた。北京オリンピック後は、「今日のウクライナ」は「明日の台湾」危機につながりかねない。今、民主主義同盟連合は危機に瀕しているが、その切り札をバイデン政権は「軍事的措置でない代償」で抑止しようとする。そのオールドメインでの戦いを米国のサリバン大統領補佐官率いるタイガーチームがホワイトハウスから展開している。タイガーチームは、ロシアによるウクライナの一部併合から大規模侵攻、体制転換に至るまで、あらゆるシナリオを想定した分析を行っている。
              </p>
              <h4>ホワイトハウスが検討する対ロ制裁シナリオ</h4>
              <p>
                タイガーチームの考える非軍事手段による抑止は、第一に、ロシアの収入源の大部分を占める天然ガスパイプラインの稼働阻止、第二に対ロ輸出管理の強化、第三にロシアの主要銀行とのドル取引停止などの制裁措置と推測される。
              </p>
              <h5>１.天然ガス輸入ストップ</h5>
              <p>
              　第一の制裁はロシアの収入源の天然ガス輸入のストップである。ロシアは石油、天然ガス、石炭などのエネルギー輸出により収入の大部分を得ているため「チョークポイント」（弁慶の泣き所）となる。しかしながら、EUは、天然ガス輸入の約3分の1をロシアに依存している。したがって、もし制裁が発動されることになれば、既に高騰している欧州のエネルギー価格が制裁発動でさらに上昇する恐れがある。また、バイデンチームは、ロシアの天然ガスをドイツに供給するパイプライン「ノルドストリーム2」の開通阻止も考えている（※）が、そうなればドイツおよびそれを支援してきた欧州企業も大打撃となろう。<br>
              　しかも、ロシアはこの制裁を見越して、中国との天然ガスの輸出を拡大する契約を結び、ガスパイプラインも次々と開通させる。中ロ首脳会談では、天然ガスの合意がなされ、ロシアは中国に天然ガスを年間100億立方メートル追加供給し、計480億立方メートルにする。実現すれば、20年のパイプラインによる供給実績の実に10倍に膨らむ。欧米の制裁がさらに中露接近を促している状況がみてとれる。<br>
              ※編注：ドイツのオーラフ・ショルツ首相は2月22日、「ノルドストリーム2」の承認作業を停止すると発表した。
              </p>
              <h5>2.最先端技術の対ロ輸出規制</h5>
              <p>
              　第二は、先端技術の対ロ輸出規制である。米国は同盟国と連携し、半導体や量子コンピューター、人工知能（AI）関連などの先端技術を対象とする。これらの製品は中国からの代替調達が難しく、ロシアの軍事・航空宇宙産業に打撃を与えることができる。また、ロシアは、軍事転用が可能な技術やソフトウェアのみならず、スマートフォンや主要な航空機・自動車部品、その他多くの分野の原材料の輸入禁止となれば、軍需・民間産業のみならず消費者や産業活動、雇用に大きなダメージを与えると考えられる。しかしながら、日本などからの輸出も規制対象となり、発動されれば世界の供給網に影響が広がる。
              </p>
              <h5>３．金融制裁</h5>
              <p>
              　第三は、金融制裁である。特に、世界の銀行決済取引網である「国際銀行間通信協会（SWIFT）」からロシアを締め出すのが最大級の制裁となるとされる。SWIFTは欧米主導で約200カ国、約1万1000の銀行、証券会社、証券取引所が参加する国際送金のためのデータ通信システムである。ベルギーに本部があるSWIFTは数兆ドル（数百兆円）にのぼる世界の銀行間の送金、決済に関するメッセージのやり取りを担っている。<br>
              　そのSWIFTがロシアの最大級の金融機関のズベルバンク（ロシア貯蓄銀行）、VTB銀行、ガスプロムバンク、アルファ銀行、ロシア直接投資基金などに的を絞った制裁を行っただけでも、ロシア経済に大打撃を与えると言われる。これに対して、ロシアは、米ドル、米国債、米国を拠点とする金融機関から外貨準備を分散させ、2021年でロシア中央銀行が保有するロシア全体の準備金のうちドル建てはわずか16.4％しかなく、3分の1はユーロ建てである。さらに、ロシアは2014年にクリミア半島併合後に西側諸国から制裁を受け、ロシア独自の決済システム「SPSF」を立ち上げ、国内送金の20％は「SPSF」を利用している。そして、金・外貨準備が6300億ドル（約72兆円）相当に達し、一定期間は制裁をしのぐ財政余力もあるといわれる。
              </p>
              <h4>相互依存の進んだ2022年世界の「グレートリセット」は</h4>
              <p>
              　ロバート・コヘイン※とジョセフ・ナイ※が「Power and　Interdependence（パワーと相互依存）」※を表わし来るべき「相互依存」の世界を予測したのは1977年であった。その時代は米ソ間に核戦争勃発の危機はらむ「冷戦」の真っ只中の時代であったが、国際政治学者の二大巨匠は冷戦後の世界を見通した。来るべき時代では米ソ間に「相互依存」が深化し、相互依存の切断は相互に被害をもたらすであろうと論じた。<br>
              　それから20年が経ち、ソ連は崩壊した。フランシス・フクヤマが「The End of the History（歴史の終わり）」を執筆し、共産主義体制と民主主義体制の相克の時代は終わリ、民主主義世界が勝利をおさめたと宣言した。そして、「パックス・デモクラチカ（民主主義による平和）」（ブルース・ラセット）がきたとまで豪語された。<br>
              　はたせるかな、それからさらに20年が経過した現在、共産主義体制は復活し、今や民主主義体制が崩壊の危機を迎えている。2022年の現在は、冷戦崩壊時よりもさらに世界中の相互依存関係は深化した。ナイやコヘインの予想をはるかに上回る「複雑相互依存」の世界が現在誕生している。その理論を適応すれば世界的に相互依存関係が深化した現在、欧米がロシアに対して経済制裁を行うと欧米自体体も大きな損失を受け、やがては世界恐慌や大規模戦争の引き金になりかねないということになる。<br>
              　そしていま、「核爆弾の選択」に匹敵するとまでいわれるSWIFTの発動を欧米がロシアに対して検討中であり、その発動は間近であると言われている。SWIFTによるロシアの銀行の締め出しは、商取引のある世界中の銀行にも大きな影響をおよぼし世界恐慌の引き金をひきかねない。それは複合的に相互依存が深化した世界社会に思いもかけぬ「グレートリセット」をもたらすであろう。
              </p>
