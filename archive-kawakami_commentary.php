              <h3 class="pt-4">危機迫るウクライナと台湾にどう向きあうか!?―民主主義同盟の危機</h3>
              <p class="text-right">川上高司「The Tokyo Post 2022年2月11日より」</p>
              <p>
              　民主主義同盟がロシアのウクライナ攻勢と中国の台湾攻勢で大きくチャレンジを受けている。<br>
              　ウクライナはロシアのプーチン大統領にとって欧米の「東方拡大」へ対する地政学上の最後の砦である。一方、台湾は中国の習近平国家主席にとって核心的利益にあたり、米国の「西方拡大」に対する守りであり、ここをとれば太平洋へ自由に出入りが可能となる。<br>
              　ここに来て、問題は中国とロシアが戦略的協調態勢をとり始めたことにある。アメリカを筆頭とする民主主義同盟国はいかに、この危機を乗り切るのかが問われている。<br>
              </p>
              <h4>米露のもたらす「危機の時代」―ウクライナと台湾</h4>
              <p>
              　中露はもともと東側陣営の同盟国であり、戦略的な地政学的類似性も外交手腕も極めて似かよっている。<br>
              　地政学的な戦略的類似性であるが、ロシアにとってウクライナはソ連時代の領土の一部であり、特にクリミア半島の港湾都市セバストポリは、黒海から地中海に抜けるロシア黒海艦隊の戦略的に重要な拠点である。冷戦末期にアメリカは「冷戦終結後もNATOの東方拡大はない」と確約した経緯がある。ウクライナはロシアにとって旧ソ連圏のなかでも経済、人口ともに最大の国家であり、欧米の「東方拡大」に対するいわば最終防衛ラインである。そのウクライナがNATOに加盟しようとしている。<br>
              　一方、中国の習近平国家主席にとって台湾は米国の「西方拡大」へ対する地政学上の要石である。1972年の米中共同宣言で台湾は中国に帰属することが認められ米軍は台湾から撤退したが、1979年の米台関係法で台湾は実質的に独立を確保している。米国はトランプ政権になってから経済安全保障という名目で台湾をとりにきている。台湾は半導体の最大の生産国であり、ここを米国に押さえられれば中国の目指す「中国製造2025」（中国内の半導体自給率を2025年までに70%に引き上げる）は達成できなくなる。台湾は中国にとっての「核心的利益」にあたり、その台湾に対して米国は国連加盟を呼びかけている。<br>
              この積極的リアリズムに基づく民主主義同盟国の行動は、防衛的リアリズムに立つ共産主義同盟国にとっては違う観点に立つわけである。
              </p>
              <h4>中露の「棍棒外交」とその目的</h4>
              <p>
              　外交的手腕の類似性は、米中両国とも「棍棒外交」を展開していることにある。「棍棒外交」とはもともとアメリカのお家芸であり、セオドア・ルーズベルト大統領のとった「穏やかに語り棍棒で脅して外交目的を達成する」というものである。<br>
              　現在、ロシアはウクライナの東部国境沿いに10万人以上の自国軍部隊を展開させ、野戦病院や燃料集積所まで造営している。ウクライナ北側のベラルーシには、ロシア軍「大隊戦術集団」を展開した。ウクライナ軍を東方面と北方面に二分させ首都キエフを脅かす二正面攻撃の準備をしているとされ、ロシアは介入準備を完了させた状態にある。<br>
              　一方中国は、たびたび台湾の防空識別圏（ADIZ）に中国軍機を頻繁に飛来させたり、近辺で軍事演習を重ねたりすることにより、米国や台湾の独立派に対して警告を発している。台湾の邱国正国防部長（国防相）は、台湾海峡の軍事的緊張は過去40年以上で「最も深刻」だとしている。中国は北京オリンピックが終わるまでは軍事的介入はないが、その後は不透明である。<br>
              　この中露両国は、アメリカなど民主主義同盟に対して共同で戦略的に「ゆさぶり」をかけているのである。<br>
              　その目的は、第一にバイデン政権の反応をみることにある。すでに、バイデン大統領はロシアが「小規模な軍事侵攻」をした場合には軍事的対応ではなく、経済制裁を科すと述べている。その場合、中国は「小規模な軍事侵攻」（例えば、台湾の離島の奪還）くらいでは米国は軍事的対応はしないと考えるであろう。<br>
              　第二に、西側同盟の絆を分断することだ。欧州連合（EU）諸国はロシアからの天然ガス供給に自国エネルギーの大半を依存する。ドイツなどは、ロシアが懲罰報復としてエネルギーの供給をストップする可能性があるため米国との共同歩調はとれないかもしれない。フランスやイタリアも怪しい。<br>
              　第三に、11月8日の中間選挙でのバイデン大統領の率いる民主党の敗北を狙う。バイデン政権がロシアのウクライナ攻勢、もしくは中国の台湾攻勢に対して何ら具体的な対策をとれず、もしは成果がでなかった場合、両国の勢いが増す。その場合、民主党が中間選挙で敗北する可能性は高く、米議会は共和党が牛耳ることとなり、「ねじれ現象」（民主党の大統領、共和党の議会）が起こり法案の成立が極めて困難となる。そうなればバイデン政権は弱体化し、民主主義国の結束は弱まることとなる。
              </p>
              <h4>軍事的手段をとらないアメリカの報復措置はどれほど有効か？</h4>
              <p>
              　ロシアがウクライナに軍事侵攻した場合、バイデン大統領は対抗措置として「軍事介入はとならい」とすでに明言している。その代わりに金融・経済制裁を準備している。14年のクリミア侵攻での対ロ制裁は対応が遅く効果がなかったため、今回は「即座に最大限の制裁措置を講じる」（米政府高官）とされる。<br>
              　制裁ケースの第一は、ロシアの主要銀行との国際取引を停止するもの。世界の通商は米国の銀行システムに連結しているため、ロシアの主要銀行は外国金融機関と取引ができずロシア経済に深刻な打撃を与える。<br>
              　第二は、世界各国の銀行間で資金決済のために必要な「SWIFT　コード」からロシア銀行を閉め出す措置で、ロシアは資金決済ができないため貿易は難しくなる。<br>
              　第三は、ロシアからの石油・ガスの輸入停止である。ロシアは国家予算の36％を石油・ガスからの収入に依存しているためロシアに致命的なダメージを与える。しかしながら欧州連合（EU）諸国は消費量の約3分の1をロシアに依存しているため大打撃を受ける。そのため、バイデン政権は、中東、北アフリカ、アジアなどの複数の国にEU諸国の不足分の供給を求める協議を行っている。もし、ロシアの供給分を他国が補うことができれば、ロシアがEUへのエネルギーをストップした場合に備えることが可能となる。<br>
              　この点に関しては、年間550億立方メートルの天然ガスを欧州に送ることができる「ノルドストリーム2」のパイプラインの起動中止がある。パイプラインの稼働を遅らせることで、ロシアは数百億ドルの収入を奪うことになる。<br>
              　その他の制裁ケースには、ロシア基幹産業向け部品サプライチェーンの遮断やプーチン大統領および側近グループの海外資産の凍結などが考えられる。また、先端技術の対ロ輸出規制も考えられ、日本からの輸出も規制対象となり、発動されれば中国に次いでロシアとのデカプリングとなり「世界の供給網」に影響が広がる。<br>
              　いずれのケースにおいても、アメリカの対露制裁措置は、ロシアとの相互依存が深化する欧州や日本などの民主主義諸国がどれだけ足並みを揃えることができるかにかかっている。
              </p>
