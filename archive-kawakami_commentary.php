              <h3 class="pt-4">「平成」から「令和」へ</h3>
              <p class="text-right">川上高司（2018/05/11）</p>
              <p>
                　「平成」から「令和」と元号が改められ日本は新たな時代に入る。元号は紀元前の古代中国で使い始められたもので、日本や朝鮮半島、ベトナムに伝えられたが中国では現在使われていない。元号は紀年法と称され、皇帝や王など君主の即位毎に変わる有限の元号は日本のみであり明治以降、天皇一代につき元号1つの「一世一元」を採用している。 <br>
                　日本では大化の改新（645年）時に「大化」が用いられたのが最初であり、それ以降1000年以上にわたり元号を継承し続けている。「令和」は「人々が心を寄せ合う中で、文化が生まれ育つ」という意味らしい。日本の元号が素晴らしいのは、儀礼的な存在にとどまらず、市民の生活に密着している点であり日本という国家の一体性を維持してきたことは間違いない。 <br>
                　新たな「令和」の典拠は、万葉集巻五に収録された梅花の宴の歌の序文「于時、初春令月、気淑風和（時は初春の令き月にして、気は美しく風は和らぎ）」にあり、大伴旅人を中心とするグループが詠んだとされる。史上初めて、元号が漢籍ではなく国書から採用された意義は大きい。 <br>
                日本は戦後、「昭和」から「平成」を経て「令和」の時代へと移行する。「昭和」の時代は、日本は敗戦を経験し独立へと向かった。そして東西冷戦に巻き込まれ、朝鮮戦争、ベトナム戦争など幾多の戦争を経験した。一方、日本は高度経済成長を迎え米国に次ぐ第二位にまで上り詰めた時代であった。 <br>
                　その「昭和」の終わりを、私は中曽根総理と迎えた。忘れもしないが、1989年1月7日午前6時に昭和天皇は崩御され、その様子をみながら我々は中曽根総理のもとに集まり今後の世界の行く末を案じた。果たせるかな－、それから間もなくベルリンの壁が11月にあっけなく崩れ東西冷戦が崩壊した。<br>
                「平成」はそのようにして始まり、新たな世界秩序が模索された。しかし米国同時多発テロ以降「テロとの闘い」が開始され世界は闇に覆われた。一方、日本はバブル経済で米国経済を一瞬ではあるが抜き「Japan As NO1」を経験した。しかしながら日本国内は平成の時代の末期には大震災に見舞われた。<br>
                　そして平成の終わりに米国を中国が凌駕しはじめ世界は混迷期に突入した。「令和」の時代に日本はどう存続するかが問われる。 
              </p>
