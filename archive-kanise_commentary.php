              <h3>ブレグジットにロシアの影</h3>
              <p class="text-right">蟹瀬誠一（2019/05/13）</p>
              <p>
                　EU離脱を巡って英国が迷走している。<br>
                　元はといえば、3年前に難民問題で窮地に立たされたキャメロン前首相が目先の選挙対策のために離脱の是非を問う国民投票に打って出たからだが、その裏に怪しげな資金を使った離脱の立て役者がいた。<br>
                　資金力にものを言わせて離脱を後押しした英実業家で富豪のアーロン・バンクスだ。<br>
                　バンクスは、右翼の英国独立党（UKIP）のファラージ党首とともに、「リーブEU(EUを去れ)」という急進的な離脱運動団体を設立し、同国史上最大の800万ポンド(約11億6千万円)もの政治資金を供与した。<br>
                　だがその資金の出所はロシアではないかという疑惑が浮上、現在は国家犯罪対策庁（NCA）の捜査対象となっている。<br>
                　手口は、「移民は侵略者だ」というような過激なメッセージをソーシャルメディアで拡散して有権者の恐怖を煽るという２０１６年米大統領選のロシア介入と酷似。バンクスは容疑を否定しているが、英ガーディアン紙によると、ロシア人女性を妻にもつ彼は国民投票前に駐英ロシア大使や要人と頻繁に接触していた。<br>
                　また、同氏はトランプの大統領当選を祝うためニューヨークのトランプ・タワーに駆けつけた最初の英国人でもある。捜査結果次第では国民投票やり直しの可能性もゼロではない。恐ろしいのは欧米に広がるロシアの影だ。
                </p>
