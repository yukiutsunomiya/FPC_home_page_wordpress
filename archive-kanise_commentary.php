              <h3>北國民主党ボストン</h3>
              <p class="text-right">蟹瀬誠一（2019/08/10）</p>
              <p>
                　イギリスの清教徒たちが自由な新世界を求めて17世紀に上陸したマサチューセッツ州の州都ボストンを久しぶりに訪れた。トランプ政権下、米国独立の原点で故ケネディ大統領生誕の地でもある街でどんな変化が起きているか知りたかったからだ。 <br>
                　折から２０２０年大統領選の民主党候補テレビ討論会がデトロイトで行われていた。ステージに立ったのは、前副大統領や上院・下院議員、州知事、市長、企業家、ベストセラー作家など20人の多彩な顔ぶれ。その中で脚光を浴びた候補のひとりにリベラル色を強く打ち出すマサチューセッツ州選出のエリザベス・ウォーレン上院議員（70）がいた。移民問題や健康保険制度に関して一部の候補から攻撃を受けたが、みごとな切り返しで形勢を逆転。知的な話しぶりで、寛容な移民政策の必要性と大企業や富裕層へ増税などを財源にして国民皆、保険制度の導入を主張していた。<br>
                　1949年、オクラホマ州オクラホマシティで決して豊かではない家庭の末っ子として生まれたエリザベスは、13歳の頃から祖母が経営するレストランで働き始めている。若くして論客としての才能を発揮し、高校生の時に州内のデベート・コンクールに優勝し、奨学金でジョージワシントン大学に進学。二児を出産後に司法試験に合格し、名門ハーバード大学法学教授も務めた努力家だ。2012年、マサチューセッツ州初の女性上院議員に選出され、地元のボストン・グローブ紙が「2020年の大統領選挙が期待できる」と書いたが、その通りになった。<br>
                　現時点の世論調査では、オバマ政権で副大統領を務めたジョー・バイデン候補が先頭を走っている。しかし最終的に民主党候補が選出されるのはまだまだ先のことだから何が起きるかわからない。6月下旬から2020年4月までに最大12回行われる予定の予備討論会や、2月から6月まで各地で開催される党員集会、予備選挙を勝ち抜いてようやく7月13日～16日の民主党全国大会で最終的に候補者が選出される。莫大な資金力と体力、精神力が求められる過酷なマラソン・レースなのだ。だが、そのお陰で有権者は候補者の人となりや考えをじっくりと吟味できる。<br>
                　迎え撃つ共和党は、今のところ現職のトランプ大統領が圧倒的に有利だとみられている。あれだけ傍若無人に振る舞い、下劣な性・人種差別発言や嘘を繰り返しているのに何故と思われるかもしれない。だが、それほど米国内の経済格差や移民問題、宗教的対立などによる分断が深刻なのだ。  <br>
                　それを大統領は逆手にとって再選戦略の中心に置いている。先月末、トランプ氏は自分に批判的な民主党下院議員の黒人が多い選挙区を「ネズミまみれの、ひどいところ」と罵った。あからさまな人種差別である。それでも、ボストン郊外で話を聞くとトランプ支持という有権者がいた。暗澹たる気持ちでその場を後にした（終）
                </p>
