              <h3>朝鮮半島雪解け</h3>
              <p class="text-right">蟹瀬誠一（2018/03/10）</p>
              <p>
                　焦り、恐れ、それとも策略？　最近まで一触即発の戦争前夜だと政府もマスコミもこぞって大騒ぎしていた朝鮮半島が今や降って湧いたような雪解けムード。武力衝突を煽っていた方々はさぞかし慌てていることだろう。<br>
                　じつは平昌五輪参加を機に北が韓国に対して平和攻勢を仕掛けてくるという話は早くから日韓の専門家の間で囁かれていた。それでもこの変わり身の早さはびっくりである。次々と弾道ミサイルを発射し６度目の核実験も強行した金正恩が破顔一笑で韓国特使団を迎え入れたかと思えば、４月には南北首脳会談を開催するというではないか。しかも軍事境界線にある板門店で！<br>
                　さらに大ニュースが飛び込んだ。訪米した韓国特使がホワイトハウスで声明を読み上げ、トランプ米大統領が５月までに金氏と会談することに応じたと発表。北が「非核化」の意向を表明し、今後は核実験や弾道ミサイル発射を自制すると約束したからだという。米大統領の決断を韓国特使がホワイトハウスの外で発表するというのは私の知る限り前代未聞だ。<br>
                　この段取り上手はどうしたことか。金正恩はてっきり暗殺を恐れて昼夜逃げ回っていると思っていたのに。どんな嘘も真実になり得る時代はこれだから始末に悪い。<br>
                　今回のデタント劇は、五輪に代表団を送る用意があるという金正恩の元旦発言から始まっている。これでスポーツの祭典は一瞬にしてホットな外交の舞台へと変容した。訪韓した北朝鮮政権幹部の中でとりわけ注目を集めたのは、「美女応援団」とともに姿を現した金正恩氏の妹、金与正。韓国政府が彼女のために「国賓Ａ」と呼ばれる最高レベルの警備体制を敷いたことからも最重要人物であったことが分かる。ちなみにペンス米副大統領は「Ｂ」、トランプ大統領の娘イバンカは「Ｃ」ランクだったという。ちょっと笑ってしまった。<br>
                　端正な顔立ちの金与正は笑みを浮かべながら「親北」の文在寅韓国大統領に訪朝を促し、米韓関係に楔を打ち込んだ。みごとな「微笑み外交」である。韓国政府関係者の話として、彼女を訪韓させたのは北朝鮮が南北関係の改善を通じて制裁圧力を突破しようとしている傍証だという解説までつけて。<br>
                　それだけではない。金正恩は、朝鮮半島の非核化のために米国と虚心坦懐に話し合う用意があり、対話継続中は核実験やミサイル発射はしないというメッセージを韓国特使団を通じて世界にしたたかに発信。外交のボールを米国のコートに投げ返して、米国の先制攻撃を封じた。<br>
                　機先を制されたトランプ大統領には米朝首脳会談への招待状まで用意されていた。この餌にトランプ氏は独断で食いついた。国務省にもペンタゴンにも相談なしに。実際の実務交渉は誰がすると思っているのだろう？　何しろ無原則でスタンドプレーが大好きな大統領である。世間の目を不倫スキャンダルやロシアゲート疑惑からそらすことが出来れば有利だと考えたに違いない。ひょっとしたらノーベル平和賞まで夢見ているかも。そういえば、韓国初のノーベル賞受賞者は２０００年に第１回南北首脳会談を実現した金大中大統領だった。ただ、金で買ったのではとの批判が絶えない。首脳会談実現のために５００億円以上を金正日に送金したという疑惑が晴れていないからだ。文大統領は五輪協力費として北に気前よく２億８０００万円を供与している。首脳会談はどうだろうと思わず邪推してしまう。<br>
                　いずれにせよ、北朝鮮は石油がないから戦争が出来ない。一番恐れているのは米国の奇襲攻撃による体制崩壊だ。それを防ぐには核武装しかないというのが一貫した北の考えである。その目的達成まで、とにかく脅しとはったりを利かして時間を稼いでいくだろう。武力衝突が起きるかどうかは米国次第だ。<br>
                　残念ながらこれまでの朝鮮半島をめぐる米国の交渉は失敗の連続。米国の担当者が目先の成果を狙うあまり部分的な合意しか出来なかったこと、そして北朝鮮が何度も約束を破って核開発を続行してきたことが主な理由である。「軍事的脅威が解消し、体制の安全が保証されれば核開発の必要がない」という北朝鮮の言葉の裏には、相変わらず「核武装、在韓米軍の撤退、朝鮮半島武力統一」という野望が透けて見える。<br>
                　さてトランプはどうするのか。彼の周りにはミサイルを撃ちたくてうずうずしている強行派もいるが、トランプ自身は悲惨な核戦争を始める度胸はないだろう。お得意のツィッターで以前にこうつぶやいていた。<br>
                　「どうして金正恩は僕を『老いぼれ』と呼んで侮辱するんだ。僕は絶対に向こうを『チビでデブ』なんて呼ばないのに。まあいいや。友達になろうと、こちらはこんなに努力している。いつの日かはそうなるかもね！」（終）
                </p>
