                <h3 class="mt-4">◎欧州から見た北朝鮮問題（下） </h3>
                <p class="text-right">細田 尚志（2018/4/20）</p>
                <p>
                    　ミサイル発射や地下核実験による瀬戸際外交を継続して地域の緊張を高めていた北朝鮮の対話路線への転換は、欧州においても評価・期待されている。しかし、北朝鮮が、心血注いで開発した体制維持の切り札である「核」を簡単に手放し、完全な非核化を受け入れるかどうかに関しては懐疑的な見解が大勢を占める。<br>
                    　もっとも、ロシアとの緊張関係が最重要課題である欧州諸国にとって、北朝鮮問題は、興味深い話題ではあるものの、その扱いは低いものにならざるを得ず、EU・北朝鮮貿易関係も、度重なる経済制裁で縮小し、欧州の対北朝鮮圧力は限定的となっている。<br>
                    　しかし、北朝鮮の核開発により、欧州でも、核抑止力や核不拡散体制の持つ意義が再注目されている点は見逃せない。そして、国際的な核不拡散レジームを維持しようとするEUの意志や、北朝鮮と外交関係を保持する欧州諸国との協力は、日本にとって重要な意義を持っている。<br>
                </p>
                <p>
                  ●　再注目される核抑止力の意義<br>
                     トランプ政権下で初めての『核体制見直し（2018NPR）』は、世界が「力の競争」時代に入り、ロシアによる「核恫喝」や中国の核戦力近代化、そして北朝鮮による核開発計画の進展が深刻性を増していることを指摘し、政権が初期に見せた米国の拡大核抑止コミットメントの揺らぎを一掃した。同文書によるNATO核共有の重要性の強調により、欧州において核抑止力の意義（含む戦術核）や核不拡散の重要性が再注目されていると言える。<br>
                    　欧州諸国としての核抑止力は、米英仏の核戦力とは別に、NATO核共有によって担保されている。平時においては、米国のB61戦術核爆弾約180発を、独、伊、オランダ、ベルギー、トルコ国内の各国空軍基地内に設けられた特別施設に米軍部隊の管理下に事前配備し、有事の際には、米、独、伊、オランダ、ベルギー空軍機によって所定の攻撃目標に「配達」されるこの制度は、冷戦後の緊張緩和及びB61爆弾の老朽化（オバマ政権下で近代化計画が承認された）にもかかわらず、NATO非核保有国も攻撃目標策定等に参画する共通の核抑止力として重要な役割を果たしてきた（勿論、最終的な使用権限は米国が掌握する）。<br>
                    　特に、NATO核共有用にアサインしているトーネード攻撃機の退役が2025年から見込まれるドイツにおいて、その後継機としてF-35導入を求めるドイツ空軍に対し、トランプ政権の自国優先主義を懸念するドイツ政府は、欧州共同開発のユーロファイター・タイフーンに核爆弾を運用するための諸改造を施すことを目論んでいるが、NATO核共有自体は、国民の85％が撤去を望んでいるにもかかわらず（2016年３月IPPNW世論調査）、引き続き欧州の核抑止力として重要な役割を果たすと認識し、制度を継続する方向である。その一方で、核不拡散体制（NPT）を通じて新たな核保有国の出現を防ぐ必要も認識し、その査察機関としてのIAEAの活用も重視している。<br>
                    　トランプ政権誕生以来、中露による米の覇権に対する挑戦が鮮明化している状況下において、トランプが自由陣営のリーダー役を降りて自国国益優先姿勢を鮮明化したことで、同盟国の安全保障に対する米国のコミットメントに対する信頼性が低下し、米国に対する反発や警戒感すら生じさせているが、目下のところ、アメリカ以外に頼れる安全保障パートナーが存在しないのは、欧州も日本も同様である。<br>
                    　その欧州諸国は、ロシアとの緊張を前に、将来的にEU諸国の部隊を構造的に統合しようと試みる「恒久構造化協力（PESCO）」などを通じて欧州としての防衛・安全保障上の自律性を確保する努力と並行して、トランプ政権による国防費のGDP２％目標要求に応え、米国製武器の調達を増やして「バイ・アメリカン」に応え、そして、アフガニスタンやシリア攻撃への戦力貢献を通じて、米国の欧州防衛に対するコミットメントを確保しようとしている。<br>
                   
                </p>   
                <p>
                  ● 　EUの対北朝鮮「クリティカル・エンゲージメント」アプローチ<br>
                  　EUは、対北朝鮮政策のゴールとして、①朝鮮半島および地域の緊張緩和、②国際核不拡散枠組みの維持、③北朝鮮内部の人権状況改善の３つを設け、対話と圧力による「クリティカル・エンゲージメント」アプローチで臨んでいる。<br>
                     　例えば、EU諸国は、ブッシュ政権のイニシアチブで開始された「拡散に対する安全保障構想（PSI）」に積極的に関与し、北朝鮮の大量破壊兵器関連物品の輸出入を海上で阻止する努力を継続してきた他、大量破壊兵器の開発・拡散の防止に向けた各種制裁の実施にも前向きであり、国連安保理決議とは別に、EU自身の制裁措置（北朝鮮の個人や企業・団体を資産凍結や渡航禁止、取引禁止の制裁対象に指定）も実施している。<br>
                    　また、EUは、1998年から北朝鮮と定期的に政治対話を継続しており、2015年６月には、第14回政治対話が平壌で開催された。EU自体は、2001年に北朝鮮と外交関係を樹立したほか、フランスとエストニアを除くすべてのEU諸国は、依然として北朝鮮と国交を保持している。特に、英、独、スウェーデン、ポーランド、チェコ、ルーマニア、ブルガリアのEU７カ国は、平壌と自国に大使館を相互設置（オーストリア、スペイン、イタリアは自国内に北朝鮮大使館のみ設置）しており、北朝鮮内部の人権状況を観察し、人権や核開発に関して平壌と意見交換する際の重要なパイプとなっている。<br>
                     　北朝鮮側も、地政学的に距離のある欧州諸国を「中立的で対話可能な相手」と認識しており、米朝交渉の事前準備や、交渉後の非核化プロセス実施にまつわる細則調整や監視の実施等で、重要な役割を果たすと期待される。<br>
                    
                </p>
                <p>
                  ●　米朝協議に望むこと<br>
                  　５月末か６月に開催されると見られる米朝協議の内容に注目が集まるが、平壌は、「核放棄よりも体制保障が先」と主張しているとも報じられており、結局、交渉は、のらりくらりとはぐらかしてきた過去の繰り返しになる恐れもある。また、一部で報道される、北朝鮮のICBM開発凍結と弾頭数制限などという部分的非核化では、米国は満足かもしれないが日本の安全保障上の脅威は除去されず、韓国の核武装による南北核均衡論も、アジアにおける核拡散の観点から深刻な懸念となる。日本に対する核の脅威が存在するままで、体良く戦後賠償だけ取られては堪らず、北朝鮮の完全な非核化が求められる。<br>             
                     　日本としては、この北朝鮮の非核化が米主導か中国主導かにかかわらず、IAEAを活用する方策を重視して、日本として北朝鮮の非核化プロセスに関与する戦略的な窓口を確保する必要がある。そのためには、すでに外務省が提案している４億円とも見積もられるIAEA査察経費の負担は重要であるが、英独仏といったイラン核合意で重要な役割を果たした欧州諸国の支持を取り付けて、核不拡散体制（NPT）の堅持や2009年４月以降北朝鮮で活動していないIAEAの査察機能を、米中に、殊更に強調することも重要であろう。<br>
                    　2005年に、欧州諸国に十分な説明もなくKEDOの解消が決定されたことに対して、未だにわだかまりを持っている高官もEUには多いと聞く。このことから、日本は、EUを６カ国協議の「準メンバー」と位置づけ、欧州諸国と密に情報交換を進めることで、相互信頼関係を深化させ、日欧が共通の脅威に対処する重要なパートナーであることを認識し合う必要もある。<br>
                     　その上で、地域安全保障環境の変化やアジア市場の有望性からアジアへの関心を増加させている欧州諸国の「アジア・ピボット」を促し、北朝鮮問題のみならず、南シナ海や東シナ海問題等のアジアの安全保障問題に可能な限りコミットさせることが求められている。これは、近年、欧州において急速に影響力を拡大してきた中国に対する警戒感が露わになっている欧州諸国を味方につける貴重な戦略的なタイミングであり、NATOとのパートナーシップを有する日本だからこそ出来る協力形態でもあろう。
                
                </p> 
                     
