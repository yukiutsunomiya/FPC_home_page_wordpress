            <div class="border-bottom border-dark">
              <h3>◎欧州から見た北朝鮮問題（上） </h3>
              <p class="text-right">細田 尚志（2018/4/20）</p>
              <p>
                　ミサイル発射や地下核実験による瀬戸際外交を継続して地域の緊張を高めていた北朝鮮の対話路線への転換は、欧州においても評価・期待されている。しかし、北朝鮮が、心血注いで開発した体制維持の切り札である「核」を簡単に手放し、完全な非核化を受け入れるかどうかに関しては懐疑的な見解が大勢を占める。
                　もっとも、ロシアとの緊張関係が最重要課題である欧州諸国にとって、北朝鮮問題は、興味深い話題ではあるものの、そのプライオリティーは低いものにならざるを得ず、EU・北朝鮮貿易関係も、度重なる経済制裁で縮小し、欧州の対北朝鮮圧力は限定的となっている。<br>
                　しかし、北朝鮮の核開発により、欧州でも、核抑止力や核不拡散体制の持つ意義が再注目されている点は見逃せない。そして、国際的な核不拡散レジームを維持しようとするEUの意志や、北朝鮮と外交関係を保持する欧州諸国との協力は、日本にとって重要な意義を持っている。<br>
              </p>
              <p>
                ●　欧州における北朝鮮問題に対する見方<br>
                　独「フランクフルター・アルゲマイン」紙のグートシェッケルは、北朝鮮側が、非核化に関する交渉の意思があることを米側に伝えたことを評価する一方で、これまで北朝鮮が、非核化の条件として米韓軍事演習の中止や在韓米軍の撤退、さらには米韓同盟の解体を求めてきたことを指摘し、完全な非核化には「Mission Kimpossible」と懐疑的である。<br>
                　また、仏「ル・モンド」紙は、核戦力の完成を宣言した2017年11月末以降の北朝鮮の姿勢転換は、国家目標を達成した金正恩の自信の表れだと分析する仏戦略研究財団研究員のアントワーヌ・ボンダズのコメントを紹介し、北朝鮮は、①米国の敵対的姿勢の終了、②「核保有国」としての国際的認知、③経済制裁の解除を求めており、金正恩の正統性の根源は、核開発と国内経済情勢にあるため、核開発が目標達成した今日、「非核化」カードによって制裁解除を要求し、経済情勢を改善するつもりだろうと指摘する。<br>
                　同様に、こちらの研究者や政府高官らと意見交換すると、北朝鮮危機に対する関心は高く、（日本政府の専門家派遣といった地道な広報努力も含めた）関係各国による欧州各国の高官やシンクタンクに対する脅威認識の共有努力により、理論上は欧州諸国も北朝鮮の弾道ミサイルの射程内に収まっているとの認識が広がり、以前よりもブリュッセルやベルリンが、北朝鮮問題を深刻に捉えるようになったことは評価される。<br>
                　もっとも、欧州諸国にとっては、英国における二重スパイ暗殺未遂事件に端を発する外交官追放合戦やロシアによるバルト海や北海における軍事活動の増加、シリアにおけるアサド政府軍による化学兵器使用と米英仏による限定的な巡行ミサイル攻撃、イラン核合意の履行確認といった諸事案の背後にある「ロシアとどう付き合うのか」という命題の方が深刻である。<br>
                　これは、ミサイル関連技術が発展し、サイバー・ドメインが最前線化している現在においても、攻撃目標が母国から遠ければ遠いほど、軍事力は弱まっていくことを主張したボールディングの「強度喪失勾配」に見られるように、依然として脅威認識の強度が、物理的な近接性に比例するからであろう。<br>
                　興味深いのは、北朝鮮との非核化交渉において、非核化合意がなされる前に、米国が北朝鮮に見返り（インセンティブ）を与えることになると、欧州諸国にとってより身近なイラン核合意の今後に悪影響を与える恐れがあるとの指摘が存在することである。これは、欧州諸国の関心が、北朝鮮やイランによる現実的な核拡散により、国際的な核不拡散レジームがなし崩し的に崩壊することを強く懸念していることを示している。<br>
              </p>
              <p>
                ●　限定的なEU・北朝鮮経済関係<br>
                　過去、南北交渉を促進するために「太陽政策」を支援していたEUは、その後の経済制裁等により、北朝鮮に対する経済的影響力を喪失し、現在、北朝鮮問題でEUが果たせる役割は極めて限定的である。例えば、2006年に2億8,000万ユーロ（約380億円）だったEU・北朝鮮間の貿易額は、2016年には十分の一の2,700万ユーロ（約37億円）にまで減少しており、さらなる経済制裁の余地はあまり残されていない。<br>
                　しかし、その一方で、欧州においても着実に北朝鮮包囲網が強化されているのも事実である。例えば、2018年2月27日、欧州議会は、国連安保理決議第2397号の内容（24ヵ月以内に北朝鮮人出稼ぎ労働者を本国に送還する）をEU法体系に反映させることを承認し、2020年1月までに、EU域内から北朝鮮の出稼ぎ労働者を一掃することを決定した。<br>
                　過去、多くの北朝鮮人労働者を受け入れていた欧州諸国は、これまでの北朝鮮に対する度重なる制裁の一環として段階的に新規ビザの発給を停止することで受け入れ人数を減らしてきたため、近年、EU諸国内で北朝鮮労働者が就労していたのはポーランドとマルタの２カ国だけになっていた。<br>
                　そのマルタも、2016年７月に、工事現場や縫製工場で就労していた出稼ぎ労働者20名の新規ビザ発給を停止し、本年１月から国連安保理議長国のポーランドも、2017年12月末に労働法を改正し、ワルシャワ郊外のジャガイモ農場やグダンスクの造船所（NATO艦艇すら修復していたらしい）で劣悪な条件下で就労していた約600人の北朝鮮出稼ぎ労働者のビザ更新を禁止しており、2020年までには、北朝鮮労働者は、欧州諸国から一掃される見込みである。<br>
                　これにより、少なくとも、賃金の90％を北朝鮮に送金していると伝えられる、欧州諸国における北朝鮮人出稼ぎ労働者の上納金が、核開発やミサイル開発に転用されることはなくなるだろう。しかし、2016年の統計によると、北朝鮮人労働者の最大受け入れ国は、ロシア（２万人）、中国（1万9,000人）、クウェート（4,500人）、カタール（3,000人）であり、ロシアや中国による国連決議の遵守が求められている（ロシアやクウェート、カタールも既に新規ビザ発給は停止している）。<br>
