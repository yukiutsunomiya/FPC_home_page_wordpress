                    　筆者が留学中の中央ヨーロッパ大学のあるハンガリーの首都ブダペストは「ドナウの真珠」、「東欧のパリ」と称されるほど歴史的建築物が整然と並ぶ美しい街である。街の北東から南西を走るアンドラーシ通りを歩くだけでも、その数奇な歴史を感じることができる。英雄広場には9世紀にハンガリーを建国した英雄たちの像がたたずみ、通りを南西に下れば右手に国立歌劇場がそびえたつ。19世紀に創設されたこの歌劇場で、オーストリア・ハンガリー二重帝国時代には、オーストリア皇帝フランツ・ヨーゼフ1世や皇妃エリザベート（愛称シシィ）がオペラを楽しんだという。ブダペスト市民の憩いの場であるコスタ・コーヒーの店舗のある建物を曲がれば、初代ハンガリー国王の名にちなんだ荘厳なイシュトバーン聖堂の後ろ姿が目に飛び込んでくる。街の目抜き通りを歩くだけでハンガリー史に思いを馳せることができるのであるが、ブダペストはまた冷戦史を学ぶにうってつけの場所でもある。<br>
