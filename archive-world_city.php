                <h3>香港という街</h3>
                <h4>活気と中国本土からの観光客にあふれているが、香港は香港だった。</h4>
                <p> 
                  　連なる大型観光バスの光景は、東京銀座の「爆買い」でも見慣れた光景だ。香港でのバスは所狭しとそこかしこに並んでいたがそれでも観光客は減っているというから、中国への返還以後どれだけの人がこの街にあふ れていたのだろうか。<br>
                  　１０月下旬、１０年ぶりに香港を訪れてみた。前回は新しい空港に驚き、ディズニーランドが開園したことに香港の発展ぶりを感じた。あれからどれほど変わっているだろうかと期待していたが、現実ははるかに先を行っていた。<br>
                  　香港は大陸の先端に張り付くような街である。したがってその発展は内陸へ向かうというよりは、横へ横へとひたすら横長に伸びていく。超高層ビルが海に向かって陸に張り付くように立ち並ぶ。高速道路と、海を渡る橋があちらこちらにできていた。<br>
                  　大は小を兼ねるということばを具現化するとこうなるのであろうか、という見本のような道路や建造物に圧倒されつつ街の中心に入っていく。<br>
                  　だが、かなり今の香港は違うという予感を空港からのタクシーの中で感じた。１０年前にタクシーに乗っても普通話（中国の共通語）を話す運転手には巡り会えなかった。今ではタクシーの運転手はなにごともないかのように普通話を話す。つまり彼らはすでに広東話と普通話を話すバイリンガルなのである。普通話と広東話は英語とフランス語ほどの違いがある。<br>
                  　今回の旅では広東話を街中で聞くことはほとんどなかった。レストランでも宿でも香港人は普通話か英語を話す。<br>
                  　だが、香港人はやはり香港人であり、中国とは違うのである。１国２制度を中国政府は採用して経済的自由を保障したが、香港人のプライドはそれだけではないのだ。話しことばは確かに普通話であるが、香港で見る漢字はどこにいっても繁体字だった。つまり普通話の簡体字は使われていないのである。そこに香港人のプライドを見ることができる。言葉は文化であり、その文化のアイデンティテ ィーである。<br>
                  　ちなみに簡体字はさっぱり理解できないが、繁体字は理解できるので大変ありがたかった。そしてその繁体字にどういうわけか、かつてまだイギリス領だったころの香港の古くて湿った香りを感じるのである。香りの街である所以だろうか。<br>
                  (川上高司・拓殖大学海外事情研究所所長・教授）
                </p>
