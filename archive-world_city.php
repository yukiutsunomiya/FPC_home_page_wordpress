                <h3>台湾・淡水で何をみるか（2017/12/28）</h3>
                <h4>「東方のベニス」と言われる港町の賑わいと緊張</h4> 
                <p>
                    　近年日本からの観光客が急増している台湾だが、人口2400万人の台湾からも日本に年間400万を超える人が訪れる親日国であることは広く知られている。一方、常に中華人民共和国に「一つの中国」の点から脅威を受けていることも周知のことである。<br>
                    　そんな台湾は台南、高雄、日月潭など豊富な観光資源を有しているが、まず人々が訪れるのは台北であろう。その台北から都市鉄道MRTで北に約40分で終点の淡水に着くが、この淡水こそが台湾の過去と現在を見事に象徴している。<br>
                    　台湾は17世紀にオランダから支配されたが、ほぼ同時期にスペインもこの地にあった。港町淡水に行くとそうした面影を残す建物がいくつか残っている。
